import express from 'express'
import {ENV} from './lib/env.js';
import path from 'path';
import { connectDB } from './lib/db.js';
import cors from 'cors';
import {functions, inngest} from './lib/inngest.js';
import {serve} from 'inngest/express';
import {clerkMiddleware} from '@clerk/express';
import { protectRoute } from './middleware/ProtectRoute.js';
import chatRoutes from './routes/chatRoutes.js';

const app =express();

const __dirname = path.resolve();

//middleware
app.use(express.json());
app.use(cors({origin:ENV.CLIENT_URL,credentials:true})); //credential true means cookies can be sent
app.use(clerkMiddleware());  // this will add req.auth object 

app.use('/api/inngest',serve({client:inngest,functions}))
app.use('/api/chat',chatRoutes)

app.get('/health',(req,res)=>{
    res.status(200).json({msg:'success from api'});
})



if(ENV.NODE_ENV==='production'){
    app.use(express.static(path.join(__dirname,'../frontend/dist')))

    app.get("/{*any}",(req,res)=>{
        res.sendFile(path.join(__dirname,"../frontend","dist","index.html"))
    })
}




const startServer=async()=>{
  try{
    await connectDB();
    app.listen(ENV.PORT,()=>console.log(`server is listening on Port ${ENV.PORT}`))
  }catch(err){
      console.error("failed to start server",err);
  }
}

startServer();